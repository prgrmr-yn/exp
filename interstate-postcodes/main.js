function getArea(postcode) {
  for (const area in postcodeAreas) {
    if (postcodeAreas[area].includes(postcode)) {
      return area;
    }
  }
  return "Unknown area";
}

inputBox.onkeyup = () => {
  let input = inputBox.value;
  if(input.length >= 3) {
    let result = getArea(input)
    display(result)
  }
}

